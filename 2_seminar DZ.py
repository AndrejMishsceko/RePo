from math import factorial

hits_on_target = 85  # —á–∏—Å–ª–æ –Ω–∞—Å—Ç—É–ø–ª–µ–Ω–∏—è —Å–æ–±—ã—Ç–∏—è (–¥–∏—Å–∫—Ä–µ—Ç–Ω–∞—è –≤–µ–ª–∏—á–∏–Ω–∞ –∏–∑ –æ—Ç—Ä–µ–∑–∫–∞ [0, ùíè])
number_of_trials = 100  # —á–∏—Å–ª–æ –∏—Å–ø—ã—Ç–∞–Ω–∏–π
shot_probability = 0.8  # –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –Ω–∞—Å—Ç—É–ø–ª–µ–Ω–∏—è —Å–æ–±—ã—Ç–∏—è ùë® –≤  –Ω–µ–∑–∞–≤–∏—Å–∏–º—ã—Ö –∏—Å–ø—ã—Ç–∞–Ω–∏—è—Ö,
opposite_probability = 0.2  # ùüè ‚Äì ùíë. –ü—Ä–æ—Ç–∏–≤–æ–ø–æ–ª–æ–∂–Ω–∞—è –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å

i = factorial(number_of_trials)/(factorial(hits_on_target)
                                 * factorial(number_of_trials - hits_on_target))
print(i)  # 2.5333847134998864e+17

x = i * (shot_probability)**(hits_on_target) * (opposite_probability)**(number_of_trials -
                                                                        hits_on_target)  # –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ, —á—Ç–æ —Å—Ç—Ä–µ–ª–æ–∫ –ø–æ–ø–∞–¥–µ—Ç –≤ –º–∏—à–µ–Ω—å, –≤—ã—Å—Ç—Ä–µ–ª–∏–≤ –æ–¥–∏–Ω —Ä–∞–∑
print("{:.10f}".format(x))  # 0.0480617937
